class TestRecommendationLifecycleTransitions:
    """Recommendations can be transitioned to applied or ignored status."""

    @pytest.mark.asyncio
    async def test_apply_recommendation_updates_status(self, lifecycle_client):
        """PATCH /api/v1/recommendations/{id}/apply changes status to 'applied'."""
        client, reco_repo = lifecycle_client

        record = RecommendationRecord(
            pod_name="cpu-pod",
            namespace="prod",
            type=RecommendationType.RIGHTSIZING_CPU,
            description="Oversized CPU",
            created_at=datetime(2026, 3, 1, tzinfo=timezone.utc),
        )
        await reco_repo.save_recommendations([record])

        # Retrieve to get the database ID
        start = datetime(2026, 1, 1, tzinfo=timezone.utc)
        end = datetime(2027, 1, 1, tzinfo=timezone.utc)
        saved = await reco_repo.get_recommendations(start=start, end=end)
        rec_id = saved[0].id

        resp = client.patch(
            f"/api/v1/recommendations/{rec_id}/apply",
            json={
                "actual_cpu_request_millicores": 300,
                "notes": "Applied via automation",
            },
        )
        assert resp.status_code == 200
        data = resp.json()
        assert data["status"] == "applied"
        assert data["actual_cpu_request_millicores"] == 300

    @pytest.mark.asyncio
    async def test_apply_recommendation_scales_realized_savings_for_partial_cpu_rightsizing(self, lifecycle_client):
        """Partial CPU rightsizing should prorate realized savings when actual values differ."""
        client, reco_repo = lifecycle_client

        record = RecommendationRecord(
            pod_name="cpu-pod",
            namespace="prod",
            type=RecommendationType.RIGHTSIZING_CPU,
            description="Oversized CPU",
            current_cpu_request_millicores=200,
            recommended_cpu_request_millicores=20,
            potential_savings_cost=180.0,
            potential_savings_co2e_grams=900.0,
            created_at=datetime(2026, 3, 1, tzinfo=timezone.utc),
        )
        await reco_repo.save_recommendations([record])

        start = datetime(2026, 1, 1, tzinfo=timezone.utc)
        end = datetime(2027, 1, 1, tzinfo=timezone.utc)
        saved = await reco_repo.get_recommendations(start=start, end=end)
        rec_id = saved[0].id

        resp = client.patch(
            f"/api/v1/recommendations/{rec_id}/apply",
            json={"actual_cpu_request_millicores": 30},
        )

        assert resp.status_code == 200
        data = resp.json()
        assert data["status"] == "applied"
        assert data["actual_cpu_request_millicores"] == 30
        assert data["cost_saved"] == pytest.approx(170.0)
        assert data["carbon_saved_co2e_grams"] == pytest.approx(850.0)

    @pytest.mark.asyncio
    async def test_ignore_recommendation_updates_status(self, lifecycle_client):
        """PATCH /api/v1/recommendations/{id}/ignore changes status to 'ignored'."""
        client, reco_repo = lifecycle_client

        record = RecommendationRecord(
            pod_name="zombie-pod",
            namespace="staging",
            type=RecommendationType.ZOMBIE_POD,
            description="Zombie pod",
            created_at=datetime(2026, 3, 1, tzinfo=timezone.utc),
        )
        await reco_repo.save_recommendations([record])

        start = datetime(2026, 1, 1, tzinfo=timezone.utc)
        end = datetime(2027, 1, 1, tzinfo=timezone.utc)
        saved = await reco_repo.get_recommendations(start=start, end=end)
        rec_id = saved[0].id

        resp = client.patch(
            f"/api/v1/recommendations/{rec_id}/ignore",
            json={"reason": "This pod is intentionally kept warm"},
        )
        assert resp.status_code == 200
        data = resp.json()
        assert data["status"] == "ignored"
        assert "intentionally" in data["ignored_reason"]

    @pytest.mark.asyncio
    async def test_apply_nonexistent_recommendation_returns_404(self, lifecycle_client):
        """Applying a non-existent recommendation ID returns 404."""
        client, _ = lifecycle_client

        resp = client.patch(
            "/api/v1/recommendations/99999/apply",
            json={"actual_cpu_request_millicores": 100},
        )
        assert resp.status_code == 404

    @pytest.mark.asyncio
    async def test_ignore_nonexistent_recommendation_returns_404(self, lifecycle_client):
        """Ignoring a non-existent recommendation ID returns 404."""
        client, _ = lifecycle_client

        resp = client.patch(
            "/api/v1/recommendations/99999/ignore",
            json={"reason": "Not applicable"},
        )
        assert resp.status_code == 404

    @pytest.mark.asyncio
    async def test_savings_summary_reflects_applied_records(self, lifecycle_client):
        """The savings summary endpoint aggregates savings from applied recommendations."""
        client, reco_repo = lifecycle_client

        record = RecommendationRecord(
            pod_name="cpu-pod",
            namespace="prod",
            type=RecommendationType.RIGHTSIZING_CPU,
            description="Oversized CPU",
            potential_savings_cost=0.50,
            potential_savings_co2e_grams=25.0,
            created_at=datetime(2026, 3, 1, tzinfo=timezone.utc),
        )
        await reco_repo.save_recommendations([record])

        start = datetime(2026, 1, 1, tzinfo=timezone.utc)
        end = datetime(2027, 1, 1, tzinfo=timezone.utc)
        saved = await reco_repo.get_recommendations(start=start, end=end)
        rec_id = saved[0].id

        # Apply the recommendation
        client.patch(f"/api/v1/recommendations/{rec_id}/apply", json={"actual_cpu_request_millicores": 200})

        # Check that savings summary reflects the applied data
        resp = client.get("/api/v1/recommendations/savings")
        assert resp.status_code == 200
