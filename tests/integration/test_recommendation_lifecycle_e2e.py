class TestRecommendationPersistence:
    """Records can be saved to and retrieved from SQLite."""

    @pytest.mark.asyncio
    async def test_save_and_retrieve_recommendation(self, sqlite_repos):
        """A saved recommendation is retrievable from the repository."""
        _, _, _, reco_repo = sqlite_repos

        recommender = Recommender()
        recs = recommender.generate_recommendations(_make_zombie_metrics())
        records = [RecommendationRecord.from_recommendation(r) for r in recs]

        count = await reco_repo.save_recommendations(records)
        assert count == len(records)

        start = datetime(2020, 1, 1, tzinfo=timezone.utc)
        end = datetime(2099, 1, 1, tzinfo=timezone.utc)
        saved = await reco_repo.get_recommendations(start=start, end=end)
        assert len(saved) == len(records)

    @pytest.mark.asyncio
    async def test_saved_record_has_database_id(self, sqlite_repos):
        """After saving, records have an auto-assigned database id."""
        _, _, _, reco_repo = sqlite_repos

        record = RecommendationRecord(
            pod_name="zombie-pod",
            namespace="staging",
            type=RecommendationType.ZOMBIE_POD,
            description="Test zombie",
        )
        await reco_repo.save_recommendations([record])

        start = datetime(2020, 1, 1, tzinfo=timezone.utc)
        end = datetime(2099, 1, 1, tzinfo=timezone.utc)
        saved = await reco_repo.get_recommendations(start=start, end=end)

        assert saved[0].id is not None
        assert isinstance(saved[0].id, int)
        assert saved[0].id > 0

    @pytest.mark.asyncio
    async def test_filter_by_type(self, sqlite_repos):
        """Filtering by recommendation type returns only matching records."""
        _, _, _, reco_repo = sqlite_repos

        await reco_repo.save_recommendations(
            [
                RecommendationRecord(
                    pod_name="zombie-pod",
                    namespace="staging",
                    type=RecommendationType.ZOMBIE_POD,
                    description="Zombie",
                ),
                RecommendationRecord(
                    pod_name="cpu-pod",
                    namespace="prod",
                    type=RecommendationType.RIGHTSIZING_CPU,
                    description="Oversized CPU",
                ),
            ]
        )

        start = datetime(2020, 1, 1, tzinfo=timezone.utc)
        end = datetime(2099, 1, 1, tzinfo=timezone.utc)
        zombies = await reco_repo.get_recommendations(start=start, end=end, rec_type="ZOMBIE_POD")

        assert len(zombies) == 1
        assert zombies[0].type == RecommendationType.ZOMBIE_POD

    @pytest.mark.asyncio
    async def test_filter_by_namespace(self, sqlite_repos):
        """Filtering by namespace returns only matching records."""
        _, _, _, reco_repo = sqlite_repos

        await reco_repo.save_recommendations(
            [
                RecommendationRecord(
                    pod_name="pod-a",
                    namespace="prod",
                    type=RecommendationType.ZOMBIE_POD,
                    description="Prod zombie",
                ),
                RecommendationRecord(
                    pod_name="pod-b",
                    namespace="staging",
                    type=RecommendationType.ZOMBIE_POD,
                    description="Staging zombie",
                ),
            ]
        )

        start = datetime(2020, 1, 1, tzinfo=timezone.utc)
        end = datetime(2099, 1, 1, tzinfo=timezone.utc)
        prod_recs = await reco_repo.get_recommendations(start=start, end=end, namespace="prod")

        assert len(prod_recs) == 1
        assert prod_recs[0].namespace == "prod"

    @pytest.mark.asyncio
    async def test_node_recommendations_are_upserted_per_node(self, sqlite_repos):
        """Node-scope recommendations with the same type must not collapse into one row."""
        _, _, _, reco_repo = sqlite_repos

        await reco_repo.upsert_recommendations(
            [
                RecommendationRecord(
                    scope="node",
                    target_node="node-a",
                    type=RecommendationType.UNDERUTILIZED_NODE,
                    description="Node A is underutilized",
                ),
                RecommendationRecord(
                    scope="node",
                    target_node="node-b",
                    type=RecommendationType.UNDERUTILIZED_NODE,
                    description="Node B is underutilized",
                ),
            ]
        )

        active = await reco_repo.get_active_recommendations()

        assert {r.target_node for r in active} == {"node-a", "node-b"}

    @pytest.mark.asyncio
    async def test_reconcile_marks_missing_active_recommendations_stale(self, sqlite_repos):
        """Active recommendations absent from the latest generation should stop being active."""
        _, _, _, reco_repo = sqlite_repos

        await reco_repo.upsert_recommendations(
            [
                RecommendationRecord(
                    scope="workload",
                    pod_name="api",
                    namespace="prod",
                    type=RecommendationType.RIGHTSIZING_CPU,
                    description="Old active recommendation",
                )
            ]
        )

        stale_count = await reco_repo.reconcile_active_recommendations([], namespace="prod")
        active = await reco_repo.get_active_recommendations(namespace="prod")
        history = await reco_repo.get_recommendations(
            start=datetime(2020, 1, 1, tzinfo=timezone.utc),
            end=datetime(2099, 1, 1, tzinfo=timezone.utc),
            namespace="prod",
        )

        assert stale_count == 1
        assert active == []
        assert history[0].status == RecommendationStatus.STALE


