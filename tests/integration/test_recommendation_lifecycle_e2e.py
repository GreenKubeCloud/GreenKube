class TestRecommendationAPIRetrieval:
    """Recommendations saved to SQLite are returned by the API."""

    @pytest.mark.asyncio
    async def test_active_recommendations_returned_by_api(self, lifecycle_client):
        """Active recommendations saved to DB are returned via /recommendations/active."""
        client, reco_repo = lifecycle_client

        # Save records directly (bypassing live generation which needs metrics in DB)
        records = [
            RecommendationRecord(
                pod_name="zombie-pod",
                namespace="staging",
                type=RecommendationType.ZOMBIE_POD,
                description="Zombie pod found via recommender",
                priority="high",
                potential_savings_cost=0.50,
                status=RecommendationStatus.ACTIVE,
                created_at=datetime.now(timezone.utc),
            )
        ]
        await reco_repo.save_recommendations(records)

        resp = client.get("/api/v1/recommendations/active")
        assert resp.status_code == 200
        data = resp.json()
        assert len(data) >= 1

        types = [r["type"] for r in data]
        assert "ZOMBIE_POD" in types

    @pytest.mark.asyncio
    async def test_recommendations_history_populated_after_save(self, lifecycle_client):
        """History endpoint returns saved records after they are written."""
        client, reco_repo = lifecycle_client

        record = RecommendationRecord(
            pod_name="zombie-pod",
            namespace="staging",
            type=RecommendationType.ZOMBIE_POD,
            description="Test zombie",
            created_at=datetime(2026, 3, 15, 12, 0, 0, tzinfo=timezone.utc),
        )
        await reco_repo.save_recommendations([record])

        resp = client.get(
            "/api/v1/recommendations/history",
            params={"start": "2026-01-01T00:00:00Z", "end": "2026-12-31T00:00:00Z"},
        )
        assert resp.status_code == 200
        data = resp.json()
        assert len(data) == 1
        assert data[0]["pod_name"] == "zombie-pod"
        assert data[0]["status"] == "active"


